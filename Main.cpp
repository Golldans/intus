#include <iostream>

using namespace std;

int main()
{
    int dummy;

    dummy = 5;

    cout << 5 + 5 << endl;
    cout << 5 * 5 << endl;
    cout << 5 - 5 << endl;
    cout << 5 / 5 << endl;
    cout << 5 % 5 << endl;
    cout << 5 / 2.0 << endl;

    dummy ++;
    cout << dummy << endl;

    cout << (double) 5 / 2 << endl;

    return 0;
}